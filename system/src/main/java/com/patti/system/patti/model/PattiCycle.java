package com.patti.system.patti.model;

public class PattiCycle {
	private int patti_id;
	private int curr_month;
	private int highest_bid;
	private int highest_bidder;
	public int getPatti_id() {
		return patti_id;
	}
	public void setPatti_id(int patti_id) {
		this.patti_id = patti_id;
	}
	public int getCurr_month() {
		return curr_month;
	}
	public void setCurr_month(int curr_month) {
		this.curr_month = curr_month;
	}
	public int getHighest_bid() {
		return highest_bid;
	}
	public void setHighest_bid(int highest_bid) {
		this.highest_bid = highest_bid;
	}
	public int getHighest_bidder() {
		return highest_bidder;
	}
	public void setHighest_bidder(int highest_bidder) {
		this.highest_bidder = highest_bidder;
	}
	
}
